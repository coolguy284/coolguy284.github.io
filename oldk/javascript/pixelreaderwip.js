draw = function() {
    if (sweep < 400 && part === 0) {
        for (var i = 0; i < 400; i ++) {
            pixels[sweep].push(get(sweep, i));
        }
        println("Initalizing: " + sweep / 4 + "%");
        sweep++;
    } else if (sweep === 400 && part === 0) {
        background(255);
        sweep = 0;
        part = 1;
        loadPixels();
        println("Initalization complete");
    } else if (sweep < 400 && part === 1) {
        println("Loading Image");
        for (var i = 0; i < 400; i ++) {
            set(sweep, i, pixels[sweep][i]);
        }
        println("loading image: " + sweep / 4 + "%");
        sweep++;
    } else if (sweep === 400 && part === 1) {
        println("loading complete");
        println("Image Rendered");
    }
};